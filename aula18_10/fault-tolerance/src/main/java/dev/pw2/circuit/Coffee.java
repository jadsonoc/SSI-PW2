package dev.pw2.circuit;

/**
 * This class represents a coffee object.
 */
public class Coffee {

    /**
     * Represents a coffee object with an ID.
     */
    private int id;

    /**
     * Returns the ID of the coffee.
     *
     * @return the ID of the coffee
     */
    public int getId() {
        return id;
    }

    /**
     * Sets the ID of the coffee.
     * @param id the ID of the coffee
     */
    public void setId(int id) {
        this.id = id;
    }

}
